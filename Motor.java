package ac;

public class Motor {
	private String brand;
	private String model;
	private int cc;
	
	public void setBrand(String brand) {
		this.brand=brand;
	}
	public String getBrand() {
		return this.brand;
	}
	public void setModel(String model) {
		this.model=model;
	}
	public String getModel() {
		return this.model;
	}
	public void setCc(int cc) {
		this.cc=cc;
	}
	public int getCc() {
		return this.cc;
	}

}
